package edu.tipos_variaveis;

public class TiposVariaveis {
  public static void main(String[] args) {

    // variaveis

    // declaracao de float
    float numero1 = 50.0F;
    // declaracao de double
    double numero2 = 50.0;

    // declaracao de String
    String palavra1 = "Palavra";
    char letra1 = 'a';

    // declaracao de numeros inteiros
    byte numero3;
    short numero4;
    int numero5;
    long numero6 = 125238534586L;

    // boolean
    boolean valor = true;

    // variable casting
    short numero7 = (short) numero6;

    // constantes
    // "final" para definir constantes, convenção é escrever o nome em uppercase
    final String PALAVRA_2 = "Palavra";

  }
}
